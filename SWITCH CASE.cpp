#include<iostream>
using namespace std;
int main (){
     int day;
     cin>>day;
      switch(day){
         case 1:
         cout<<"white"<<endl;
         break;

         case 2:
         cout<<"red"<<endl;
         break;

         case 3:
         cout<<"yellow"<<endl;
         break;

         default:
         cout<<"default case"<<endl;
         


      }
      return 0;

}


